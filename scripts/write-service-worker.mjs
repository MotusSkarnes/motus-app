/**
 * After `vite build`, writes dist/sw.js with a precache list parsed from dist/index.html.
 * Enables offline shell + cached static assets; does not cache Supabase API.
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const dist = path.join(root, "dist");
const indexPath = path.join(dist, "index.html");

if (!fs.existsSync(indexPath)) {
  console.error("write-service-worker: dist/index.html missing — run vite build first.");
  process.exit(1);
}

const html = fs.readFileSync(indexPath, "utf8");
const urls = new Set([
  "/",
  "/index.html",
  "/manifest.webmanifest",
  "/favicon.svg",
  "/apple-touch-icon.png",
  "/pwa-192.png",
  "/pwa-512.png",
  "/pwa-512-maskable.png",
]);

for (const m of html.matchAll(/(?:src|href)="(\/assets\/[^"]+)"/g)) {
  urls.add(m[1]);
}

const PRECACHE_URLS = JSON.stringify([...urls]);

const swSource = `/* Generated by scripts/write-service-worker.mjs — do not edit by hand */
const CACHE = "motus-precache-v1";
const PRECACHE_URLS = ${PRECACHE_URLS};

self.addEventListener("install", (event) => {
  event.waitUntil(
    (async () => {
      const cache = await caches.open(CACHE);
      for (const url of PRECACHE_URLS) {
        try {
          await cache.add(url);
        } catch (err) {
          console.warn("[motus-sw] precache failed:", url, err);
        }
      }
      await self.skipWaiting();
    })()
  );
});

self.addEventListener("activate", (event) => {
  event.waitUntil(
    (async () => {
      const keys = await caches.keys();
      await Promise.all(keys.filter((k) => k !== CACHE).map((k) => caches.delete(k)));
      await self.clients.claim();
    })()
  );
});

self.addEventListener("fetch", (event) => {
  const req = event.request;
  if (req.method !== "GET") return;

  if (req.mode === "navigate") {
    event.respondWith(
      (async () => {
        try {
          const res = await fetch(req);
          if (res && res.ok) {
            const cache = await caches.open(CACHE);
            await cache.put("/index.html", res.clone());
          }
          return res;
        } catch {
          const cached = await caches.match("/index.html");
          return cached || new Response("Offline", { status: 503, headers: { "Content-Type": "text/plain; charset=utf-8" } });
        }
      })()
    );
    return;
  }

  const url = new URL(req.url);
  if (url.origin !== self.location.origin) return;

  event.respondWith(
    (async () => {
      const cached = await caches.match(req);
      if (cached) return cached;
      try {
        const res = await fetch(req);
        if (res && res.ok) {
          const copy = res.clone();
          const cache = await caches.open(CACHE);
          await cache.put(req, copy);
        }
        return res;
      } catch {
        return (await caches.match(req)) || Response.error();
      }
    })()
  );
});

self.addEventListener("push", (event) => {
  event.waitUntil(
    (async () => {
      let title = "Motus";
      let body = "";
      let data = { url: "/" };
      if (event.data) {
        try {
          const json = await event.data.json();
          title = json.title || title;
          body = json.body || "";
          if (json.url) data = { url: String(json.url) };
        } catch {
          try {
            body = await event.data.text();
          } catch {
            body = "";
          }
        }
      }
      await self.registration.showNotification(title, {
        body,
        icon: "/pwa-192.png",
        badge: "/pwa-192.png",
        data,
      });
    })()
  );
});

self.addEventListener("notificationclick", (event) => {
  event.notification.close();
  const raw = event.notification.data && event.notification.data.url ? String(event.notification.data.url) : "/";
  const abs = new URL(raw, self.location.origin).href;
  event.waitUntil(
    (async () => {
      if (self.clients.openWindow) await self.clients.openWindow(abs);
    })()
  );
});
`;

fs.writeFileSync(path.join(dist, "sw.js"), swSource, "utf8");
console.log("write-service-worker: wrote dist/sw.js with", urls.size, "precache entries.");
